import { ChainId, NETWORKS } from '@human-protocol/sdk';

import { USDT_CONTRACT_ADDRESS } from '../constants';

export const getTokenAddress = (
  chainId: ChainId,
  name: string
): string | undefined => {
  switch (name) {
    case 'usdt':
      return USDT_CONTRACT_ADDRESS[chainId];
    case 'hmt':
      return NETWORKS[chainId]?.hmtAddress;
  }
};
